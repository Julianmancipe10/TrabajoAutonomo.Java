package estructurasIterativasWhile;

public class ejemploUno {
	
	   public static void main(String[] args) {
	        int contador = 1;
	        //bucle whill  contador de 1a5  
	        while (contador <= 5) {
	            System.out.println("Contador: " + contador);
	            contador++;
	        }
	    }
}
